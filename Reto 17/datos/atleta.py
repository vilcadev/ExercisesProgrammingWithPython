class atleta:
    
    # Definimos el constructor para la construccion de nuestros objetos atleta,
    # si bien es un metodo opcional, siempre es mejor declararlo
    def __init__(self) -> None:
        pass
    # Método que solo nos devuelve el string "run"
    def run(self):
        return "run"
